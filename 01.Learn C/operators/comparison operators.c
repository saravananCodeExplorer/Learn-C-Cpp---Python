//3.Comparison Operators

// '=='equal operators
#include<stdio.h>
int main(){
	int a = 20;
	int b = 20;
	printf("'=='condition is true :%d\n", a == b);
	
	//'!='not equal  operator
	int _a = 25;
	int _b = 25;
	printf("'!='condition is false :%d\n", _a != _b);
	
	// '>'Greater than operator
	int num1=20;
	int num2=10;
	printf("'>'condition is true :%d\n", num1>num2);
	
	// '<'Less than operator
	int num1_=10;
	int num2_ = 20;
	printf("'<'condition is true :%d\n", num1_ < num2_);
	
	// '>='Greater than or equal to operator
	int x=50;
	int y=30;
	printf("'>='condition is true :%d\n", x >= y);
	
	// '<='less than or equal to operator
	int x_=20;
	int y_=25;
	printf("'<='condition is true :%d\n", x_ <= y_);
	return 0;
	
	 
}
