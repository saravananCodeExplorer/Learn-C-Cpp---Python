//Logical operators
// '&&'and operator
#include<stdio.h>
int main(){
	int a = 10;
	int b = 20;
	printf("the condition is true:%d\n",(a>5 && b<30));

//'||'or operator	
	int _a = 10;
	int _b = 20;
	printf("the condition is true:%d\n",(a>5 || b>30));
	
//'!'not operator	
	int a_ = 10;
	int b_ = 10;
	printf("the condition is true:%d\n",!(a_ == b_));
}	 
